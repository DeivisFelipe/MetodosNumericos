using namespace std;

#define NUM 15

int main()
{
    int n = 15, i, j;
    double x[NUM], y[NUM];
    double x2[NUM], yx[NUM], sx = 0, sx2 = 0;
    double a0, a1;
    double ly[NUM], lyx[NUM], sly = 0, slyx = 0;
    double a, b;
    
    x[0] = 3.04;
    x[1] = 4.884;
    x[2] = 7.674;
    x[3] = 9.637;
    x[4] = 10.52;
    x[5] = 13.18;
    x[6] = 15.45;
    x[7] = 18.63;
    x[8] = 20.11;
    x[9] = 19.04;
    x[10] = 21.95;
    x[11] = 23.09;
    x[12] = 25.1;
    x[13] = 27.28;
    x[14] = 33.78;
    
    y[0] = 0.01052;
    y[1] = 0.02192;
    y[2] = 0.05033;
    y[3] = 0.1118;
    y[4] = 0.1332;
    y[5] = 0.3797;
    y[6] = 1.035;
    y[7] = 3.03;
    y[8] = 3.703;
    y[9] = 3.751;
    y[10] = 7.651;
    y[11] = 10.45;
    y[12] = 24.0;
    y[13] = 47.15;
    y[14] = 490.9;;
    
    for(i = 0; i<n; i++){
        sx += x[i];
    }
    
    for(i = 0; i<n; i++){
        x2[i] = x[i] * x[i];
        ly[i] = log(y[i]);
        lyx[i] = x[i] * ly[i];
        sx2 += x2[i];
        sly += ly[i];
        slyx += lyx[i];
    }
    
    a0 = (sx2*sly - slyx*sx)/(double (n) * sx2 - sx * sx);
    a1 = (double (n) * slyx - sx*sly)/(double (n) * sx2 - sx * sx);
    a = exp(a0);
    b = exp(a1);
    
    cout << "\t\tx;log(y);x2;log(y)x" << endl;
    for(i = 0; i<n; i++){
        cout << "\t\t" << x[i] << ";" << ly[i] << ";" << x2[i] << ";" << lyx[i] << endl;
    }
    
    cout << "-------------------------------------------------------------------------" << endl;
    cout << "Somas;" << sx << ";" << sly << ";" << sx2 << ";" << slyx << endl;
    cout << "-------------------------------------------------------------------------" << endl;
    
    cout << "a0 = " << a0 << ";a1 = " << a1 << endl;
    cout << "a = " << a << ";b = " << b << endl;
}

